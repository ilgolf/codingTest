package programmers_step2;

// 전화번호부에 적힌 전화번호 중, 한 번호가 다른 번호의 접두어인 경우가 있는지 확인하려 합니다.
// 전화번호가 다음과 같을 경우, 구조대 전화번호는 영석이의 전화번호의 접두사입니다.
//
// 구조대 : 119
// 박준영 : 97 674 223
// 지영석 : 11 9552 4421
// 전화번호부에 적힌 전화번호를 담은 배열 phone_book 이 solution 
// 함수의 매개변수로 주어질 때, 어떤 번호가 다른 번호의 접두어인 경우가 있으면 false를 
// 그렇지 않으면 true를 return 하도록 solution 함수를 작성해주세요.

import java.util.*;

public class Solution1 {
	
	public boolean solution(String[] phone_book) {
        Map<String, Integer> map = new HashMap<>();
        
        int k = 0;
        for(String n : phone_book) {
        	map.put(n, k);
        	k++;
        }
        
        for(int i=0; i<phone_book.length; i++) {
        	for(int j=0; j<phone_book[i].length(); j++) {
        		if(map.containsKey(phone_book[i].substring(0, j))) { return false; }
        	}
        }
        return true;
    }
	
	public static void main(String[] args) {
		Solution1 s = new Solution1();
		
		String[] phone_book = {"119", "97674223", "1195524421"};
		
		boolean answer = s.solution(phone_book);
		System.out.println(answer);
	}
}
